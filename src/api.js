export const api = {
  getProperties: async () => {
    await new Promise((r) => setTimeout(r, 300));
    return mockProperties;
  },

  getPropertyById: async (id) => {
    await new Promise((r) => setTimeout(r, 250));
    return mockProperties.find((p) => p._id === id) || null;
  },

  searchProperties: async ({ query = '', minPrice, maxPrice, beds, city, type } = {}) => {
    await new Promise((r) => setTimeout(r, 250));
    const q = query.trim().toLowerCase();

    return mockProperties.filter((p) => {
      const inText =
        !q ||
        p.address.street.toLowerCase().includes(q) ||
        p.address.city.toLowerCase().includes(q) ||
        p.description.toLowerCase().includes(q);

      const inCity = !city || p.address.city.toLowerCase() === city.toLowerCase();
      const inType = !type || p.propertyType.toLowerCase() === type.toLowerCase();
      const inBeds = !beds || Number(p.bedrooms) >= Number(beds);
      const inMin = !minPrice || Number(p.price) >= Number(minPrice);
      const inMax = !maxPrice || Number(p.price) <= Number(maxPrice);

      return inText && inCity && inType && inBeds && inMin && inMax;
    });
  },
};
